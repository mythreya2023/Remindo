    <script >
    $(document).ready(function(){
    var submit=false;
    if(!submit){
      $(".signup_form_submit").submit(function(e){
      e.preventDefault();
      submit=true;
      let fullname=$("#fullname").val();
      let Email=$("#email").val();
      let Pass=$("#pwd").val();
      let Cpass=$("#cpwd").val();
      let Country=document.getElementById("u_country");
      let Gender=document.getElementById("u_gender");
      let date=document.getElementById("dateinmonthofbirth");
      let Month=document.getElementById("monthinyearofbirth");
      let Year=document.getElementById("yearofbirth");
      var CountryName=Country.options[Country.selectedIndex].text;
      var CountryTime=Country.options[Country.selectedIndex].value;
      var UserGender=Gender.options[Gender.selectedIndex].text;
      var dateofbirth=date.options[date.selectedIndex].value;
      var Monthofbirth=Month.options[Month.selectedIndex].value;
      var Yearofbirth=Year.options[Year.selectedIndex].value;
      let submitBtn=$("#signup_Btn").val();
      
      $.ajax({
          url:"http://localhost/remindo/loginSignup/signup.inc.php",
          method:"post",
          data:{
        name:fullname,
        mail:Email,
        u_country:CountryName,
        u_gender:UserGender,
        pwd:Pass,
        cpwd:Cpass,
        TimeZone:CountryTime,
        birthdate:dateofbirth,
        birthmonth:Monthofbirth,
        birthyear:Yearofbirth,
        signup_submit:submitBtn
          },
          beforeSend:()=>{
              $(".sgnup_pr_ldr").show();
              $(".signup-submit-btn-spantag").text("Signing up...");
          },
          success:(data)=>{
              $(".sgnup_pr_ldr").hide();
            if(data!=1){
            $("#signup-status-box").html(data);
            submit=false;
              $(".signup-submit-btn-spantag").text("Sign up");
            }else{
            submit=true;
              $(".signup-submit-btn-spantag").text("Signed up Successfully!...");
            $(".signup-popup-container").hide();
            window.open("http://localhost/remindo/signin","_self");
            }
          },
      });
    e.stopImmediatePropagation();
    return false;
    });
    }else{
     submit=false;   
    }
    });

    </script>
